public class MeasureSwitcher {

    double meterToCentimeter(double a){
        return a*100;
    }

    double meterToMillimeter(double a){
        return a*1000;
    }

    double centimeterToMeter(double a){
        return a/100;
    }

    double millimeterToMeter(double a){
        return a/1000;
    }

    int hourToMinute(int a){
        return a*60;
    }

    int minuteToSec(int a){
        return a*60;
    }

    int secToMillisec(int a){
        return a*1000;
    }
}
